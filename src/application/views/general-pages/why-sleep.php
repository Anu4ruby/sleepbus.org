<div class="aboutbord">
<p>
At sleepbus we have a simple focus; provide safe, temporary overnight accommodation to those sleeping rough in Australia. Our aim is to get people off the street and keep them safe and healthy until they can get back on their feet.
</p>

<p>
Our accommodation is not a long term solution. What we do provide is a safe, temporary and comfortable place to sleep for the night.
</p>

<p>
sleepbus is distinct, yet complementary to existing efforts from other organisations supporting Australians experiencing, or at risk of homelessness. Our work aims to fill a 'gap', rather than overlapping or replicating activities that support the urgent needs of people in Australia.
</p>
</div>

<div class="programbox2">
<h2><img alt="" src="/images/icon14.png" /></h2>

<p>Sleep plays a vital role in good health and wellbeing; and getting enough quality sleep can help protect a person&rsquo;s mental and, physical health, and ultimately their safety. For those sleeping rough, getting a good nights sleep is virtually impossible and can contribute to long-term homelessness and more.</p>

<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
<div class="row">
<div class="col-lg-4 col-md-4 col-sm-4 col-xs-12 changebox">
<div class="changeboximg"><img alt="" src="/images/icon15.png" /></div>

<h2>Mental Health</h2>

<p>Sleep deficiency can harm a person&rsquo;s mental health over time; Low self-esteem, social isolation, and the exacerbation or development of specific mental health disorders including schizophrenia, depression, bipolar disorder and post-traumatic stress disorder.</p>
</div>

<div class="col-lg-4 col-md-4 col-sm-4 col-xs-12 changebox">
<div class="changeboximg"><img alt="" src="/images/icon16.png" /></div>

<h2>Physical Health</h2>

<p>Sleep deficiency can harm a person&rsquo;s physical health over time. For example, ongoing sleep deficiency can raise the risk for some chronic health problems such as, Diabetes, Heart Health, Breathing Problems and Breast Cancer.</p>
</div>

<div class="col-lg-4 col-md-4 col-sm-4 col-xs-12 changebox">
<div class="changeboximg"><img alt="" src="/images/icon17.png" /></div>

<h2>Safety</h2>

<p>Many sleeping on the street are subjected to terrible weather, harassment, bullying, being robbed and worse. Those sleeping on the street also run a high risk of drug and alcohol abuse, further affecting their safety and wellbeing.</p>
