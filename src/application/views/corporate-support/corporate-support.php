        <div class="col-lg-9 col-md-9 col-sm-7 col-xs-12 fundingboxright">
        <h2>Bus Fuel</h2>

        <p>Membership Program</p>

        <p>Our goal for the 100% program model is ambitious. To support our growth, we bring together a like-minded community of business people and philanthropists to fund our operating budget on a regular&nbsp;basis. Bus Fuel is a membership program where donors give a set amount to our operating costs each year. Their support paves the way for us to continue doing what many will say is impossible: scale the organisation using our 100% model and end the need for people to sleep rough in Australia.</p>
        </div>
        </div>
        </div>
        </div>

