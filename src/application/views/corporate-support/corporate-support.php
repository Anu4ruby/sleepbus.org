        <h1>Corporate support</h1>

        <div class="fundingtoptext">We partner with corporations for matching programs, public campaigns, employee engagement and other fundraising ideas tailored to the business&#39; market and brand. It is this support that makes it possible for sleepbus to do the work that needs to be done and allows us to always use 100% of public donations to fund sleepbus projects.</div>

        <div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
