        <div class="col-lg-9 col-md-9 col-sm-7 col-xs-12 fundingboxright">
        <h2>sleepbus Investor</h2>

        <p>Our sleepbus Investors are donors that recognise the impact of a major gift to our operational expenses. Like any startup business, we need investors who believe in, and support our business model. The 100% Model. But we can&rsquo;t offer stock options or the promise of a big buyout; so instead, our investors ROI is measured in the amount of safe sleeps we provide; one sleepbus can provide 8,030 safe sleeps per year and our aim is 300+ buses providing more than 2,000,000 safe sleeps and ending the need for people to sleep rough in Australia.</p>
        </div>
        </div>
        </div>
        </div>

