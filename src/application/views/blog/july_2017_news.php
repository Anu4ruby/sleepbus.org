				<h3>SLEEPBUS | HARD WORK GETS NOTICED</h3>
				<p>I am thrilled to announce that sleepbus<sup>&reg;</sup> is a finalist for the 2017 Telstra Business Awards. The Awards dinner will be held on Friday 28th July, at which time winners will be announced; finters crossed</p>
				<p>For the past 25 years the Telstra Business Awards has been recognising, rewarding and empowering Australia’s best small and medium businesses and, more recently, charities. The judging process is intense, with both a written and P&amp;L submission, site visit and interview. Finalists are throughly assessed on all areas of their business including financial acumen, cretivity and innovation, risk management, social responsibilities and leadership</p>
				<p>It truly means a lot to be recognised for the hard work that has been done behind the scenes to ensure sleepbus® can deliver on its vision of ending the need for people to sleep rough in Australia. It has't been easy; being the "first in the world" brings with it apprehension from Governments and other agencies with whom we seek to partner. The many hurdles that have been placed in front of us have frustrated me no end, but despite the delays, this doesn’t stop what must be done and so we forge ahead, committed to making it happen.</p>
				<p>Becoming a finalist in a prestigious award like the Telstra Business Awards provides sleepbus with further opportunities to open doors that are currently closed. It confirms that we are a legitimate organisation doing things the right wat and it is hoped that this will give confidence to Governments and other Agencies, working to solve the serious homelessness issue facing our country, that sleepbus<sup>&reg;</sup> is the real deal.</p>
				<p>A huge thank you to all the sleepbus<sup>&reg;</sup> family who continue providing support. Together we are rolling a small snowball up a very steep hill, we are almost at the top, and then watch what happens when we get on a roll.</p>
				<p>Keep an eye on Facebook, Twitter and Instagram for the updates on the awards.</p>
				<p>Thank you</p>
				<p>Simon, Founder | CEO</p>
