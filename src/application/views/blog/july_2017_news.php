					<h3>MEET THE TEN YEAR OLD WE ALL WANT TO BE</h3>
					<p>This is Lincoln Barclay, a ten year old primary school student in Melbourne, who didn’t like what he was seeing in the city, so he decided to do something about it.</p>
					<p>Lincoln asked his mum Mel to contact sleepbus to see if it would be ok for him to raise money for safe sleeps. He also wanted to present the sleepbus Charity to his school Principle to get the whole school involved. So here is a ten year old, arranging a meeting with his school Principal to pitch a charity because he didn’t like what he was seeing... how is that not the best thing you have ever heard.</p>
					<p>Lincoln didn’t just have a meeting with the Principal and Vice Principal of his school, he had a successful meeting, gaining their full support. Lincoln has started a Fundraising Campaign on the sleepbus website where he hopes to raise $1,000 for safe sleeps. He’s been washing cars in his neighbourhood during school holidays and is seeking sponsors for a 5km run he is planning. At the time of writing, he had already raised more than $400.</p>
					<p>If you’d like to support this 10 year old legend by donating to his campaign, click the link below, or if you’d like to start your own campaign, that would be awesome too.</p>
